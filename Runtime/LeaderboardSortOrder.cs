namespace PlatformFacade
{
    /// <summary>
    /// Specifies the sort order for leaderboard entries
    /// </summary>
    public enum LeaderboardSortOrder
    {
        /// <summary>
        /// Entries are sorted from highest score to lowest score (descending)
        /// </summary>
        HighestFirst,
        
        /// <summary>
        /// Entries are sorted from lowest score to highest score (ascending)
        /// </summary>
        LowestFirst
    }
}